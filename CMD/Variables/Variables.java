public class Variables
{
  public static void main(String args[])
  {
    int Num1 = 2;
    int Num2 = 10;
    int Resultado= 0;
    Resultado = Num1 + Num2;
    System.out.println("El Resultado De La Suma Es: " + Resultado);
    Num1 = 2;
    Num2 = 5;
    Resultado= 0;
    Resultado= Num1 * Num2;
    System.out.println("El Resultado De La Multiplicacion Es: " + Resultado);
  }
}