public class Matrices
{
  public static void main (String args[])
  {
   int num[][]= new int[2][2];
    //Filas        Filas
          //Columnas     Columnas
   num[0][0]= 5;
   num[0][1]= 2;
   num[1][0]= 2;
   num[1][1]= 5;

   System.out.print  ("["+ num[0][0] +"]");
   System.out.println("["+ num[0][1] +"]");
   System.out.print  ("["+ num[1][0] +"]");
   System.out.print  ("["+ num[1][1] +"]");




  }
}