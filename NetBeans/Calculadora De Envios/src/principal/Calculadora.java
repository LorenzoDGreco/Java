    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

            String numerito = "";
            monto = Integer.parseInt(TextoMonto.getText());

            if (opc_1.isSelected()) { //Catamarca
                iva = (float) 2.5;
                CalculosDel80OP();
            }
            if (opc_2.isSelected()) {
                iva = (float) 4.25;
                CalculosOP();
            }
            if (opc_3.isSelected()) {
                iva = (float) 3.5;
                CalculosOP();
            }
            if (opc_4.isSelected()) {
                iva = (float) 3;
                CalculosOP();
            }
            if (opc_5.isSelected()) {
                iva = (float) 3;
                CalculosOP();
            }
            if (opc_6.isSelected()) {
                iva = (float) 6;
                CalculosOP();
            }
            if (opc_7.isSelected()) {
                iva = (float) 5;
                CalculosOP();
            }
            if (opc_8.isSelected()) {
                iva = (float) 2.5;
                CalculosDel80OP();
            }
            if (opc_9.isSelected()) {
                iva = (float) 5;
                CalculosOP();
            }

            if (resultado != 0) {
                TextoAnteResultado.setVisible(true);
                TextoResultado.setVisible(true);
                MontoIngresado.setVisible(true);
                TextoMontoFinalizado.setVisible(true);
                TextoResultadoTotal.setVisible(true);
                TextoResultadoTotalIngresar.setVisible(true);

                String ResultadoString = "";
                ResultadoString = Float.toString(resultado);
                TextoResultado.setText(ResultadoString);

                ResultadoString = Float.toString(monto);
                TextoMontoFinalizado.setText(ResultadoString);

                ResultadoString = Float.toString(resultadoConIva);
                TextoResultadoTotalIngresar.setText(ResultadoString);
                
            } else {
                MontoIngresado.setVisible(false);
                TextoMontoFinalizado.setVisible(false);
                TextoAnteResultado.setVisible(false);
                TextoResultado.setVisible(false);
                TextoResultadoTotal.setVisible(false);
        TextoResultadoTotalIngresar.setVisible(false);
            }
        
    }//GEN-LAST:event_jButton1ActionPerformed

