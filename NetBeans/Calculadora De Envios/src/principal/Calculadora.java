    private void CalculosOP() {
        float valorIntermedio = 0;
        valorIntermedio = monto * iva;
        resultado = valorIntermedio / 100;
        resultadoConIva = resultado + monto;
    }

    private void CalculosDel80OP() {
        float valorIntermedio = 0;
        valorIntermedio = monto * 80;
        valorIntermedio = valorIntermedio / 100;
        valorIntermedio = valorIntermedio * iva;
        resultado = valorIntermedio / 100;
        resultadoConIva = resultado + monto;
    }

